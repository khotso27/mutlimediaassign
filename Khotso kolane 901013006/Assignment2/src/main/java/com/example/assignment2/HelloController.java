    @FXML
    void playVideo(MouseEvent event) {

        player.play();
        player.setRate(1);
    }


    @FXML
    void pauseVideo(MouseEvent event) {
        player.pause();
    }



    @FXML
    void stopVideo(MouseEvent event) {
        player.stop();
    }

    @FXML
    void BackwardVideo(MouseEvent event) {
        player.seek(player.getCurrentTime().add(Duration.seconds(-10)));
    }

    @FXML
    void ForwardVideo(MouseEvent event) {
        player.seek(player.getCurrentTime().add(Duration.seconds(+10)));
    }


    @FXML
    void exitVideo(MouseEvent event) {
        Platform.exit();
    }


